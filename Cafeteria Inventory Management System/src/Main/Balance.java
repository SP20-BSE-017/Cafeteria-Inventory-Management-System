package Main;

public class Balance {
    private int balance;
    private int stockExpenses;
    private int otherExpenses;

    public int getBalance() {
        return balance;
    }

    public void setBalance(int balance) {
        this.balance = balance;
    }

    public int getStockExpenses() {
        return stockExpenses;
    }

    public void setStockExpenses(int stockExpenses) {
        this.stockExpenses = stockExpenses;
    }

    public int getOtherExpenses() {
        return otherExpenses;
    }

    public void setOtherExpenses(int otherExpenses) {
        this.otherExpenses = otherExpenses;
    }
}
