package Main;

public class ReorderItems {
    private int itemID;
    private int supplierID;
    private int itemOrderQuantity;
    private int itemOrderPrice;

    public int getItemID() {
        return itemID;
    }

    public void setItemID(int itemID) {
        this.itemID = itemID;
    }

    public int getSupplierID() {
        return supplierID;
    }

    public void setSupplierID(int supplierID) {
        this.supplierID = supplierID;
    }

    public int getItemOrderQuantity() {
        return itemOrderQuantity;
    }

    public void setItemOrderQuantity(int itemOrderQuantity) {
        this.itemOrderQuantity = itemOrderQuantity;
    }

    public int getItemOrderPrice() {
        return itemOrderPrice;
    }

    public void setItemOrderPrice(int itemOrderPrice) {
        this.itemOrderPrice = itemOrderPrice;
    }
}
